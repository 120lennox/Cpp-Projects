#include <iostream>
#include <string>

int main(){
    long num;
    short number;
    double var;
    std::string message;

    std::cout<<"size of long = "<<sizeof(num)<<std::endl;
    std::cout<<"size of short = "<<sizeof(number)<<std::endl;
    std::cout<<"size of double = "<<sizeof(var)<<std::endl;
    std::cout<<"size of string = "<<sizeof(message)<<std::endl;
}